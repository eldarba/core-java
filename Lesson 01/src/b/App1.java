public class App1 {

	public static void main(String[] args) {
		// casting - convert from one type to another

		{
			int x = 5;
			long y = x; // implicit casting
			int z = (int) y; // explicit casting
		}
		
		{
			double x = 5.68;
			int y = (int)x; // explicit casting

		}

	}
}
